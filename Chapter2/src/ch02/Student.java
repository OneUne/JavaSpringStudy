package ch02;

// class명은 PascalCase를 따른다.
// package명은 lowercase.
// Student 파일에 여러 클래스를 만들 수 있으나, public class는 Student 하나여야 함.
// 파일명과 이름이 같은!
public class Student {
	int studentNumber; // 변수나 함수는 camelCase.
	String studentName; //String은 Java의 기본 자료형이 아니라 Java 제공 클래스.
	int majorCode;
	String majorName;
	int grade;
}
